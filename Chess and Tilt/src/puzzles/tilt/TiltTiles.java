package puzzles.tilt;

/**
 * TiltTiles
 * This interface contains constants for the types of tiles/cells in a Tilt board.
 *
 * @author Ryan O'Malley
 * @github cro5058
 */
public interface TiltTiles {
    // Empty space
    char BLANK = '.';
    // Gray blocker
    char BLOCKER = '*';
    // Blue slider
    char BLUE = 'B';
    // Exit hole
    char EXIT = 'O';
    // Green slider
    char GREEN = 'G';
}
